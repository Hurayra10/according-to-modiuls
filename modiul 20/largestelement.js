/*find the largest element from an array;



function largestElements(numbers){
    let largest = numbers[0];

    for(let i = 0; i < numbers.length; i++){
        let element = numbers[i];
        if(element > largest){
            largest = element;
        }
    } return largest;
}

console.log(largestElements([40, 2, 64, 24, 0, 7, 87,100]))*/






